// عملگرهای ریاضی در جاوااسکریپت
 
// توضیح         عملگرها 
//----------------------
//  +	        جمع
//  -	        تفریق
//  *	        ضرب
//  **	        توان (ES2016)
//  /	        تقسیم
//  %	        باقی مانده 
//  ++	        افزایش
//  --	        کاهش

// --------------------------------

// عملگرهای نسبت دهی در جاوااسکریپت

//   شبیه        مثال         عملگرها          
//------------------------------------
//      =	        x = y	    x = y
//      +=	        x += y	    x = x + y
//      -=	        x -= y	    x = x - y
//      *=	        x *= y	    x = x * y
//      /=	        x /= y	    x = x / y
//      %=	        x %= y	    x = x % y
//      **=	        x **= y	    x = x ** y

var x = 10;
x += 5;  // ----> 15

// عملگرهای رشته در جاوااسکریپت
// عملگر +
// برای اضافه کردن
// یا به عبارتی بهم وصل کردن دو رشته هم استفاده میشه

var txt1 = "Yashar";
var txt2 = "Habibi";
var txt3 = txt1 + " " + txt2;//---> Yashar Habibi

// عملگر +=
// برای اضافه یا وصل کردن دو رشته هم استفاده میشه

