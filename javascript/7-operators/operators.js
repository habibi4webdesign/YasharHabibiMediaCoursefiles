// عملگرهای جاوااسکریپت
// = عملگر انتساب
// یه مقداری رو به یه متغیر نسبت میده

var x = 10;

// عملگر بعلاوه +
// اعداد رو با هم جمع میکنه

var x = 5;
var y = 2;
var z = x + y; //---> 7

//عملگر ضرب *
// اعداد رو در هم ضرب میکنه

var x = 5;
